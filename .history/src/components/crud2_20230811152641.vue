<template>
    <div>
      <div class="row">
        <div class="col-9">
          <h1>Danh sách User</h1>
          <table class="table">
            <thead>
              <tr>
                <td>
                  <input v-model="nameSearch.Name" placeholder="Tìm kiếm theo tên..." />
                  <button @click="handleSearch">Tìm kiếm</button>
                </td>
                <td>Name</td>
                <td>Slug</td>
                <td>Description</td>
                <td>Website</td>
                <td>Chức năng</td>
              </tr>
            </thead>
            <tr v-for="user in users" :key="user.Id">
              <td>{{ user.Id }}</td>
              <td>{{ user.Name }}</td>
              <td>{{ user.Slug }}</td>
              <td>{{ user.Description }}</td>
              <td>{{ user.Website }}</td>
              <td>
                <button @click="editUser(user)">Chỉnh sửa</button>
                <button @click="deleteUser(user.Id)">Xóa</button>
              </td>
            </tr>
          </table>
        </div>
        <div class="col-2">
          <h1 v-if="editUser.Id">Chỉnh sửa user</h1>
          <h1 v-else>Thêm user mới</h1>
          <form @submit.prevent="editUser.Id ? updateUser() : addNewUser()">
            <label for="Name">Name</label>
            <input
              class="form-control"
              type="text"
              id="Name"
              v-model="editUser.Name"
              v-if="editUser.Id"
              required
            />
            <input
              class="form-control"
              type="text"
              id="Name"
              v-model="newUser.Name"
              v-else
              required
            />
            <!-- ... Các phần còn lại tương tự -->
            <button class="btn-block bg-success" type="submit">
              {{ editUser.Id ? 'Lưu' : 'Thêm User' }}
            </button>
          </form>
        </div>
      </div>
    </div>
  </template>
  
  <script>
  import { ref, onMounted } from 'vue';
  import axios from 'axios';
  
  export default {
    name: 'YourComponentName',
    setup() {
      const nameSearch = ref({ Name: '' });
      const users = ref([]);
      const newUser = ref({
        Name: '',
        Slug: '',
        Description: '',
        Website: '',
      });
      const editUser = ref({
        Id: '',
        Name: '',
        Slug: '',
        Description: '',
        Website: '',
      });
  
      const handleSearch = async () => {
        try {
          const response = await axios.post('https://api.tranhai.net/api/manufacturer/_search', nameSearch.value);
          users.value = response.data.data;
        } catch (err) {
          console.error(err);
          alert('Tìm kiếm thất bại');
        }
      };
  
      const getUsers = async () => {
        try {
          const response = await axios.post('https://api.tranhai.net/api/manufacturer/_search', {
            Page: 1,
            PageSize: 10,
          });
          users.value = response.data.data;
        } catch (error) {
          alert('Lỗi khi lấy dữ liệu user:', error);
        }
      };
  
      // Các phần còn lại của methods tương tự
  
      onMounted(() => {
        getUsers();
      });
  
      return {
        nameSearch,
        users,
        newUser,
        editUser,
        handleSearch,
        getUsers,
        // ...Các hàm khác
      };
    },
  };
  </script>
  
